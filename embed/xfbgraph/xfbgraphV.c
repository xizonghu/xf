static void write_font(XF_Bgraph *bg, uint8 x, uint8 y, uint8 const *bits, uint8 h) {
    uint32 *fb = &mfb[y][x >> XF_BGRAPH_CELL_OFFSET];  //将x转为fb位置 (x >> 5) = (x / 32)
    uint32 shft = (x & 0x1FU);     //(x & 0x1FU) = (x % 32)
    for (y = 0; y < h; ++y, fb += (XF_BGRAPH_PIXEL_LENGHT >> XF_BGRAPH_CELL_OFFSET)) {  //(LCD_LENGHT >> 5) = (32)
        *fb |= ((uint32)bits[y] << shft);
        if (shft > 24U) {  //一个字节8bit，超出本cell，需要去填充下一个cell
            *(fb + 1) |= ((uint32)bits[y] >> (32U - shft));
        }
    }
}

static void write_font_cover(XF_Bgraph *bg, uint8 x, uint8 y, uint8 const *bits, uint8 h) {
    uint32 *fb = &mfb[y][x >> XF_BGRAPH_CELL_OFFSET];
    uint32 shft = (x & 0x1FU);
    uint32 mask1 = ~((uint32)0xFFU << shft);
    uint32 mask2;
    if (shft > 24U) {
        mask2 = ~(0xFFU >> (32U - shft));
    }
    for (y = 0; y < h; ++y, fb += (XF_BGRAPH_PIXEL_LENGHT >> XF_BGRAPH_CELL_OFFSET)) {
        *fb = ((*fb & mask1) | ((uint32)bits[y] << shft));
        if (shft > 24U) {
            *(fb + 1) = ((*(fb + 1) & mask2)
                | ((uint32)bits[y] >> (32U - shft)));
        }
    }
}

