int XF_BgraphSetText(XF_Bgraph *bg, uint8 x, uint8 y, char *str, XF_BgraphColor color) {
    for (; *str != '\0'; ++str, x += 8) {
        uint8 const *ch = &F8X16[*str - ' '][0];
        uint8 rev[16] = {0};
        if(XF_BGRAPH_COLOR_BLACK == color) {
		    uint8 i = 0;
		    for(i = 0; i < 16; i++)rev[i] = ~ch[i];
		    ch = rev;
        }
        write_font_cover(bg, x, y, ch, 16);
    }
    return 0;
}

